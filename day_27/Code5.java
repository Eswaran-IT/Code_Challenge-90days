package day_27;

public class Code5 {
public static void main(String[] args) {
	        double number = 4.0;
	        double exponent = 2.0;

	        // Square root
	        double squareRoot = Math.sqrt(number);

	        // Exponentiation
	        double result = Math.pow(number, exponent);

	        // Absolute value
	        double absValue = Math.abs(-10.5);

	        // Rounding
	        long rounded = Math.round(6.8);
	        double floorValue = Math.floor(7.6);


	        System.out.println("Square Root: " + squareRoot);
	        System.out.println("Exponentiation: " + result);
	        System.out.println("Absolute Value: " + absValue);
	        System.out.println("Rounded Value: " + rounded);
	        System.out.println("Floor Value: " + floorValue);
	     

}
}
